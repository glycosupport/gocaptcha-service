var DefaultEmbeddedFonts = NewEmbeddedFontsStorage(fonts.DefaultEmbeddedFontsFS)

var fontsSimple = DefaultEmbeddedFonts.LoadFontsByNames([]string{
	"abduction.ttf",
	"hacked.ttf",
	"heartless.ttf",
	"dotmartrix.ttf",
	"linebeam.ttf",
	"modular.ttf",
	"neonize.ttf",
	"positive.ttf",
	"tourner.ttf",
	"vogue.ttf",
	"perspect.ttf",
	"cubic.ttf",
})

var fontsAll = append(fontsSimple)

func randFontFrom(fonts []*truetype.Font) *truetype.Font {
	fontCount := len(fonts)

	if fontCount == 0 {
		fonts = fontsAll
		fontCount = len(fontsAll)
	}
	index := rand.Intn(fontCount)
	return fonts[index]
}

